number_1=int(input("Enter first number "))
number_2=int(input("Enter second number "))
solve=input("add,subtract,multiply,divide " )


if solve=="add" :
    print(number_1+number_2)

elif solve=="subtract":
    print(number_1-number_2)

elif solve=="multiply":
    print(number_1*number_2)
    
elif solve=="divide":
    print(number_1/number_2)